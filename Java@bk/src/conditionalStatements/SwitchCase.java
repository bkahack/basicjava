package conditionalStatements;

public class SwitchCase {

	public static void main(String[] args) {
		int dayOfWeek =3;
		switch (dayOfWeek) {
		case 1 :
			System.out.println("this is my working day ");
			break;
		case 2 :
			System.out.println("I m in home ");
			break;
		case 3:
			System.out.println(" i play football ");
			break ;
			default :
			System.out.println("i dont remember ");
			
		
		}
			
	}

}
