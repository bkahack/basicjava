package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {
		int x = 10;
		double y=x;
		float z=x;
		long p=x;
		System.out.println(x);			//	(Widening) Automatic TypeCasting is there. 
		System.out.println(y);
		System.out.println(z);
		System.out.println(p);
	}

}
